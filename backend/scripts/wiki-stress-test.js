async function run() {
  console.log(`\n🧪 Wiki Stress Test`);
  console.log(`   Team: ${TEAM_ID}`);
  console.log(`   Documents: ${DOCUMENTS.length}`);
  console.log(`   Scope: ${SCOPE_ID}\n`);

  let totalTriples = 0;
  let errors = 0;

  for (let i = 0; i < DOCUMENTS.length; i++) {
    const doc = DOCUMENTS[i];
    const preview = doc.substring(0, 60).replace(/\n/g, ' ');
    console.log(`[${i + 1}/${DOCUMENTS.length}] "${preview}..."`);

    try {
      const result = await RavenService.previewRemember(SCOPE_ID, USER_ID, doc);
      const tripleCount = result.extractedTriples?.length || 0;
      console.log(`  Extracted: ${tripleCount} triples`);

      if (tripleCount > 0) {
        const confirmed = await RavenService.confirmRemember(result.previewId, [], USER_ID);
        const created = confirmed.triplesCreated?.length || 0;
        const updated = confirmed.triplesUpdated?.length || 0;
        totalTriples += created + updated;
        console.log(`  Confirmed: ${created} new, ${updated} updated`);
      }
    } catch (err) {
      console.error(`  ERROR: ${err.message}`);
      errors++;
    }

    // Small delay between documents
    await new Promise(r => setTimeout(r, 1500));
  }

  console.log(`\n=== INGESTION COMPLETE ===`);
  console.log(`Total triples: ${totalTriples}`);
  console.log(`Errors: ${errors}`);

  // Check SST state
  const tree = await SSTService.getTree(TEAM_ID);
  console.log(`\nSST nodes: ${tree.length}`);
  const roots = tree.filter(n => n.is_root);
  const children = tree.filter(n => !n.is_root);
  console.log(`  Roots: ${roots.length}`);
  console.log(`  Children: ${children.length}`);
  console.log(`\n=== SST TREE ===`);
  tree.forEach(n => {
    const indent = '  '.repeat(n.depth);
    const marker = n.is_root ? '◉' : '├';
    console.log(`${indent}${marker} ${n.name} (${n.triple_count} triples)`);
  });

  // Final counts
  const [tc, cc] = await Promise.all([
    db.query('SELECT COUNT(*) as c FROM triples WHERE team_id = $1 AND status = $2', [TEAM_ID, 'active']),
    db.query('SELECT COUNT(*) as c FROM concepts WHERE team_id = $1', [TEAM_ID]),
  ]);
  console.log(`\nFinal: ${tc.rows[0].c} active triples, ${cc.rows[0].c} concepts`);

  // Test some queries
  console.log(`\n=== RETRIEVAL TESTS ===`);
  const testQueries = [
    'How far is Jupiter from the Sun?',
    'Which planet is the hottest?',
    'Is Pluto a planet?',
    'What moons might have life?',
    'How many moons does Saturn have?',
    'Tell me about the asteroid belt',
    'What is the Great Red Spot?',
    'How old is the Solar System?',
  ];

  for (const q of testQueries) {
    console.log(`\nQ: "${q}"`);
    try {
      const answer = await RavenService.ask(SCOPE_ID, USER_ID, q);
      console.log(`A: ${answer.answer.substring(0, 200)}...`);
      console.log(`   Confidence: ${answer.confidence}, Triples used: ${answer.factsUsed?.length || 0}`);
    } catch (err) {
      console.log(`   ERROR: ${err.message}`);
    }
  }

  await db.end();
  process.exit(0);
}

run().catch(err => {
  console.error('Fatal:', err);
  process.exit(1);
});
